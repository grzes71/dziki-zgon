        opt h+

;---------------------------------------
; Atari XL/XE
; ANTIC E (160x192, 4 kolory)
;---------------------------------------

SCREEN      = $4000

        org $2000

start

;---------------------------------------
; Wyłączenie BASIC
;---------------------------------------

        lda #0
        sta 559         ; SDMCTL

;---------------------------------------
; Kolory (z title-0_colors.asm → shadow registers)
;---------------------------------------

        icl "title-0_colors.asm"

;---------------------------------------
; Display List
;---------------------------------------

        lda #<DLIST
        sta 560

        lda #>DLIST
        sta 561

;---------------------------------------
; DMA ON
;---------------------------------------

        lda #$2e
        sta 559
        sta $D400

Forever

        jmp Forever

;---------------------------------------
; Display List
;---------------------------------------

        org $3000

Title0Data = SCREEN

        icl "title-0_displaylist.asm"

;---------------------------------------
; Obraz
;---------------------------------------

        org SCREEN

        ins "title-0.bin"

        run start